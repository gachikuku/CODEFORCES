#include <bits/stdc++.h>

using namespace std;

#define ll long long


void solve(){

    ll n=0;
    
    cin>>n;
    
    if(n%4==0) cout<<"YES\n";
    
    else cout<<"NO\n";
}


int main(){

    ios::sync_with_stdio(0);
    cin.tie();
    
    int t;
    
    cin>>t;
    
    while(t--) solve();
}